package session;

import domain.model.Convenio;
import domain.model.Proyecto;
import java.util.List;

/**
 *
 * @author Ricardo
 */
public class ProyectoSession {
    private List<Convenio> convenioList;
    private Convenio convenio;
    private List<Proyecto> proyectoList;
    private Proyecto proyecto; 

    public ProyectoSession() {
    }

    public List<Convenio> getConvenioList() {
        return convenioList;
    }

    public void setConvenioList(List<Convenio> convenioList) {
        this.convenioList = convenioList;
    }

    public Convenio getConvenio() {
        return convenio;
    }

    public void setConvenio(Convenio convenio) {
        this.convenio = convenio;
    }

    public List<Proyecto> getProyectoList() {
        return proyectoList;
    }

    public void setProyectoList(List<Proyecto> proyectoList) {
        this.proyectoList = proyectoList;
    }

    public Proyecto getProyecto() {
        return proyecto;
    }

    public void setProyecto(Proyecto proyecto) {
        this.proyecto = proyecto;
    }
}
